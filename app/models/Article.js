class Article {
    constructor(title, subtitle, body, draft, featured, image, tags, created_date, author) {
        this.title = title;
        this.subtitle = subtitle;
        this.body = body;
        this.draft = draft;
        this.featured = featured;
        this.author = author;
        this.created_date = created_date;
        this.img = img;
        this.tags = tags;
    }
}